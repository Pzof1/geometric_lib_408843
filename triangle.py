def area(a, h):
    """
    area(a)
    
    Принимает значения a,h и возвращает значение площади
    
    Пример: area(5,10) = 25
    """
    return a*h/2

def perimeter(a, b, c):
    """ 
    perimeter(a,b,c)
    
    Принимает значения a,b,c и возвращает значение периметра

    Пример: perimeter(1,2,3) = 6
    """
    return a+b+c
